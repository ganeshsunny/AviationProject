create database AirAsia;
use AirAsia;
/*City Table*/
CREATE TABLE CITY
(CNAME VARCHAR(15) NOT NULL,
STATE VARCHAR(15), 
COUNTRY VARCHAR(30),
PRIMARY KEY(CNAME));
/*Airports Table*/
CREATE TABLE AIRPORT
(AP_NAME VARCHAR(100) NOT NULL,
STATE VARCHAR(15), 
COUNTRY VARCHAR(30),
CNAME VARCHAR(15),
PRIMARY KEY(AP_NAME),
FOREIGN KEY(CNAME) REFERENCES CITY(CNAME));
/*AirLines Table*/
CREATE TABLE AIRLINE
(AIRLINEID VARCHAR(3) NOT NULL,
AL_NAME VARCHAR(50),
THREE_DIGIT_CODE VARCHAR(3),
ICAO VARCHAR(3),
CALL_SIGN VARCHAR(50),
PRIMARY KEY(AIRLINEID));
/*Flights Table*/
CREATE TABLE FLIGHT
(FLIGHT_CODE VARCHAR(10) NOT NULL,
SOURCE VARCHAR(3),
DESTINATION VARCHAR(3),
ARRIVAL VARCHAR(10),
DEPARTURE VARCHAR(10),
STATUS VARCHAR(10),
DURATION VARCHAR(30),
FLIGHTTYPE VARCHAR(10),
LAYOVER_TIME VARCHAR(30),
NO_OF_STOPS INT,
AIRLINEID VARCHAR(3),
PRIMARY KEY(FLIGHT_CODE),
FOREIGN KEY(AIRLINEID) REFERENCES AIRLINE(AIRLINEID));
/*Routes Table*/
CREATE TABLE ROUTES
(ROUTE_ID INT NOT NULL,
ARRIVAL VARCHAR(10),
DEPARTURE VARCHAR(10),
AIRLINEID VARCHAR(3),
PRIMARY KEY(ROUTE_ID),
FOREIGN KEY(AIRLINEID) REFERENCES AIRLINE(AIRLINEID));
/*Passenger Table*/
CREATE TABLE PASSENGER
(PID INT NOT NULL,
FNAME VARCHAR(20),
LNAME VARCHAR(20),
ADDRESS VARCHAR(100),
PHONE INT,
AGE INT,
SEX VARCHAR(1),
FLIGHT_CODE VARCHAR(10),
PRIMARY KEY(PID),
FOREIGN KEY(FLIGHT_CODE) REFERENCES FLIGHT(FLIGHT_CODE));
/*Employee Table*/
CREATE TABLE EMPLOYEE1
(SSN INT NOT NULL,
FNAME VARCHAR(20),
LNAME VARCHAR(20),
ADDRESS VARCHAR(100),
PHONE INT,
AGE INT,
SEX VARCHAR(1),
JOBTYPE VARCHAR(30),
ASTYPE VARCHAR(30),
ETYPE VARCHAR(30),
SHIFT VARCHAR(20),
POSITION VARCHAR(30),
AP_NAME VARCHAR(100),
PRIMARY KEY(SSN),
FOREIGN KEY(AP_NAME) REFERENCES AIRPORT(AP_NAME));
/*Salary according to job type*/
CREATE TABLE EMPLOYEE2
(JOBTYPE VARCHAR(30) NOT NULL,
SALARY INT,
PRIMARY KEY(JOBTYPE));
/*Ticket Table*/
CREATE TABLE TICKET
(TICKET_NUMBER INT NOT NULL,
SOURCE VARCHAR(3),
DESTINATION VARCHAR(3),
DATE_OF_BOOKING DATE,
DATE_OF_TRAVEL DATE,
SEATNO VARCHAR(5),
CLASS VARCHAR(15),
DATE_OF_CANCELLATION DATE,
PID INT,
FOREIGN KEY(PID) REFERENCES PASSENGER(PID));
/*Pilots Information*/
CREATE TABLE PILOT
(PILOT_ID INT NOT NULL,
PILOT_NAME VARCHAR(20),
PILOT_TYPE VARCHAR(20),
PILOT_EXPERIENCE INT,
PILOT_SALARY INT,
PRIMARY KEY(PILOT_ID));
/*Flight Pilot Information*/
CREATE TABLE FLIGHT_PILOT
(FLIGHT_CODE VARCHAR(10),
PILOT_ID INT,
FOREIGN KEY(FLIGHT_CODE) REFERENCES FLIGHT(FLIGHT_CODE),
FOREIGN KEY(PILOT_ID) REFERENCES PILOT(PILOT_ID));
/*Transactions Table*/
CREATE TABLE TRANSACTION
(TR_ID INT,
TR_TYPE VARCHAR(20),
TR_SOURCE VARCHAR(20),
CARDNUMBER LONG,
UPI_ID VARCHAR(20),
TR_DATE DATE,
PID INT,
FOREIGN KEY(PID) REFERENCES PASSENGER(PID));
